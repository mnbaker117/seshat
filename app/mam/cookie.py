"""
MAM session cookie management.

The `mam_id` cookie is the ONLY auth mechanism MAM exposes — every
HTTP call (.torrent grab, dynamic-seedbox ping, search probe) is
authenticated by attaching that cookie as an HTTP header. The cookie
is generated by the user via Preferences → Security on the MAM site,
locked to either an IP or an ASN, and (in practice) needs to be
rotated about once a month.

This module owns the auth layer the pipeline needs: HTTP client, IP
register flow, and cookie validation probe.

Two non-obvious choices, both load-bearing — do NOT change without a
real-world end-to-end check first:

  1. The `curl/8.0` User-Agent is intentional. MAM has bitten us with
     subtle UA-based rejection before, and this UA is verified against
     MAM end-to-end.
  2. `http2=False` is explicit. httpx can speak HTTP/2 when the `h2`
     package is installed; we pin HTTP/1.1 because that's what's been
     verified against MAM and we don't want variable transport
     behavior creeping in.

The validation probe deliberately uses MAM's search endpoint (a tiny
JSON POST) rather than scraping HTML. The endpoint is stable and
returns clear status codes.
"""
from __future__ import annotations

import asyncio
import json
import logging
from typing import Awaitable, Callable, Optional
from urllib.parse import urlparse

import httpx

_log = logging.getLogger("seshat.mam.cookie")


# ─── MAM endpoint constants ──────────────────────────────────

MAM_BASE_URL = "https://www.myanonamouse.net/"
MAM_SEARCH_URL = "https://www.myanonamouse.net/tor/js/loadSearchJSONbasic.php"
MAM_DYNIP_URL = "https://t.myanonamouse.net/json/dynamicSeedbox.php"


def _is_mam_url(url: str) -> bool:
    """True if `url` targets a myanonamouse.net host (any subdomain).

    Defense-in-depth host check used to gate URL fetches that attach
    the MAM session cookie. Replaces the previous `if "myanonamouse.net"
    in url` substring checks (CodeQL py/incomplete-url-substring-
    sanitization #25 / #28) which were bypassable via attacker URLs
    containing the literal substring elsewhere — e.g.
    `https://attacker.com/?u=myanonamouse.net` would have routed
    through MAM auth and leaked the session cookie to attacker.com.

    Accepts the apex `myanonamouse.net` plus any subdomain
    (`www.`, `t.`, `cdn.` — all three are in active use). Rejects
    anything that doesn't parse as a URL with a hostname.
    """
    if not url:
        return False
    try:
        host = (urlparse(url).hostname or "").lower()
    except Exception:
        return False
    if not host:
        return False
    return host == "myanonamouse.net" or host.endswith(".myanonamouse.net")

